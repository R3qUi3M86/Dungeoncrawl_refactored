package com.codecool.dungeoncrawl.display;

public class CameraField {
    private final int X;
    private final int Y;

    public CameraField(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }

    public int getX() {
        return X;
    }

    public int getY() {
        return Y;
    }
}
